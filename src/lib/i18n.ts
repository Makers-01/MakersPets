export type UiLang = "zh" | "en";

export function getLang(value: string | string[] | undefined): UiLang {
  const raw = Array.isArray(value) ? value[0] : value;
  return raw === "en" ? "en" : "zh";
}

export function localeForLang(lang: UiLang) {
  return lang === "en" ? "en-US" : "zh-CN";
}

